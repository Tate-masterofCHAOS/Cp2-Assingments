def stat_update(current_koro):
    decrease = random.randint(1, 3)
    if decrease == 1:
        current_koro.hunger -= random.randint(20, 50)
    elif decrease == 2:
        current_koro.happiness -= random.randint(20, 50)
    elif decrease == 3:
        current_koro.energy -= random.randint(20, 50)
    if current_koro.hunger <= 30:
        print(f'{current_koro.name} is hungry')
        current_koro.status = 'hungry'
    if current_koro.happiness <= 30:
        print(f'{current_koro.name} is sad')
        current_koro.status = 'sad'
    if current_koro.energy <= 30:
        print(f'{current_koro.name} is tired')
        current_koro.energy = 0
        current_koro.status = 'tired'
    return current_koro
