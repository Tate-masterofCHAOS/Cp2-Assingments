class pet:
    def __init__(self, name, shade, time_alive, hunger, happiness, energy, max_hunger, max_happiness, max_energy, status):
        self.name = name
        self.shade = shade
        self.time_alive = time_alive
        self.hunger = hunger
        self.happiness = happiness
        self.energy = energy
        self.max_hunger = max_hunger
        self.max_happiness = max_happiness
        self.max_energy = max_energy
        self.status = status

    def __str__(self):
        return f'{self.name} is a {self.shade} faced Koro\nmax hunger: {self.max_hunger} \nMax happiness: {self.max_happiness} \nMax energy: {self.max_energy}'
    
    def feed(self):
        print(pet_sprites['pet_hungry'])
        print(f'{self.name} is having a little snack on some metal pipe he found')
        time.sleep(5)
        print('50 hunger restored')
    
    def sleep(self):
        print(pet_sprites['pet_sleepy'])
        print(f'{self.name} is having a little nap')
        time.sleep(5)
        print('50 energy restored')

    def play(self):
        print(pet_sprites['pet_play'])
        print(f'You chase after {self.name} with a knife for a bit')
        time.sleep(5)
        print('50 energy restored')

    def stat_check(self):
        return f"__________________________________________________________________________________________________________________________________________________\nStats for {self.name} \nShade: {self.shade} \nTime Alive: {self.time_alive} \nHappiness: {self.happiness}/{self.max_happiness} \nHunger: {self.hunger}/{self.max_hunger} \nEnergy: {self.energy}/{self.max_energy} \nStatus: {self.status}"




def pet_selection():
    with open('Pet_simulator/koro.csv', 'r') as file:
        reader = csv.reader(file)
        pets = []
        for row in reader:
            if row[0] != 'name':  # Assuming the first row contains headers
                pets.append(row)
                print(row)

        choice = input('Please put the name of the koro you would like to use (case-sensitive): ')
        for pet_data in pets:
            if pet_data[0] == choice:
                current_koro = pet(
                    name=pet_data[0],
                    shade=pet_data[1],
                    time_alive=int(pet_data[2]),
                    hunger=int(pet_data[3]),
                    happiness=int(pet_data[4]),
                    energy=int(pet_data[5]),
                    max_hunger=int(pet_data[6]),
                    max_happiness=int(pet_data[7]),
                    max_energy=int(pet_data[8]),
                    status=pet_data[9]
                )
                return current_koro

        print('Sorry, that pet does not exist.')
        choice = input('Press 1 to return or 2 to create a new pet: ')
        if choice == '1':
            return pet_selection()
        elif choice == '2':
            return pet_creation()
         
